    <h2>Las mujeres de la Bauhaus</h2>
    <p>
      Desde su fundación en 1919, la Bauhaus se presentó como una escuela progresista que promovía la igualdad, invitando tanto a hombres como a mujeres a formar parte de su comunidad. Sin embargo, en la práctica, las mujeres se enfrentaron a desafíos y limitaciones. A pesar de que el manifiesto de Walter Gropius proclamaba la integración de todas las artes y oficios sin distinción de género, muchas estudiantes fueron orientadas a talleres considerados "apropiados" para ellas, como tejido, cerámica o encuadernación, mientras que disciplinas como la arquitectura y la carpintería estaban dominadas por hombres.
       <br><br>
      Aun así, las mujeres de la Bauhaus lograron destacarse y dejar un legado fundamental. En el taller de tejido, por ejemplo, convirtieron esta disciplina en un laboratorio de innovación donde se exploraron materiales y patrones que luego influirían en el diseño textil moderno y en la industria. Figuras como Gunta Stölzl, única mujer en dirigir un taller en la escuela, y Anni Albers, reconocida por sus textiles experimentales, demostraron que el arte funcional podía alcanzar gran prestigio.
Otras mujeres, como Marianne Brandt, rompieron barreras al ingresar en talleres dominados por hombres, como el de metalurgia, diseñando objetos icónicos como lámparas y juegos de té que hoy son símbolos del diseño moderno.
 <br><br>
Aunque enfrentaron desigualdades, las mujeres de la Bauhaus no solo participaron, sino que ayudaron a transformar la estética y los valores de la escuela. Su trabajo sigue siendo una inspiración para el diseño contemporáneo y un recordatorio de la importancia de la diversidad y la inclusión en el ámbito creativo.
    </p>
