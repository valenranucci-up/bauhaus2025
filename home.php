    <h2>La influencia actual</h2>
    <p>
      A más de cien años de su fundación, la Bauhaus continúa siendo una fuente de inspiración para diseñadores,
      arquitectos y artistas de todo el mundo.
      Su visión integradora, que unió arte, tecnología y funcionalidad, es visible en tendencias actuales como
      el minimalismo, el diseño sustentable y la producción en serie inteligente, que busca optimizar recursos
      sin perder calidad.
  <br><br>
      En arquitectura, sus principios se reflejan en edificios que privilegian la luz, la geometría pura y la
      conexión con el entorno. En el diseño industrial y digital, la idea de crear productos accesibles, intuitivos
      y estéticamente agradables es un reflejo directo de su filosofía.
      Incluso en la educación, muchas escuelas de diseño actuales continúan utilizando métodos inspirados en la Bauhaus,
      fomentando la experimentación, el trabajo interdisciplinario y la unión entre teoría y práctica.
   <br><br>
      La Bauhaus no fue solo una escuela: fue un movimiento cultural que cambió para siempre la forma en que entendemos
      el diseño, y su impacto sigue presente en cada objeto y espacio que habitamos.
    </p>
